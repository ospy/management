package com.manage.entity;

public class FileInfo {
	String newbalance;
	String i_base_price;
	String i_spec_price;
	String s_filename;
	String s_path;
	String s_password;
	String filesize;
	String filetype;
	
	public String getFilesize() {
		return filesize;
	}
	public void setFilesize(String filesize) {
		this.filesize = filesize;
	}
	public String getNewbalance() {
		return newbalance;
	}
	public void setNewbalance(String newbalance) {
		this.newbalance = newbalance;
	}
	public String getS_filename() {
		return s_filename;
	}
	public void setS_filename(String s_filename) {
		this.s_filename = s_filename;
	}
	public String getS_path() {
		return s_path;
	}
	public void setS_path(String s_path) {
		this.s_path = s_path;
	}
	public String getS_password() {
		return s_password;
	}
	public void setS_password(String s_password) {
		this.s_password = s_password;
	}
	public String getI_base_price() {
		return i_base_price;
	}
	public void setI_base_price(String i_base_price) {
		this.i_base_price = i_base_price;
	}
	public String getI_spec_price() {
		return i_spec_price;
	}
	public void setI_spec_price(String i_spec_price) {
		this.i_spec_price = i_spec_price;
	}
	public String getFiletype() {
		return filetype;
	}
	public void setFiletype(String filetype) {
		this.filetype = filetype;
	}
	
}
